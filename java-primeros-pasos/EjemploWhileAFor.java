
public class EjemploWhileAFor {
	
	public static void main(String[] args) {
        int contador = 0;
        while(contador <= 10) {
            System.out.println(contador);
            contador++;
        }
        
        
        for (int contadorFor = 0; contadorFor <=10; contadorFor++) {
        	System.out.println(contadorFor);
        }
        
        
    }

}
